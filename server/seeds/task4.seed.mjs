const insert = db.prepare(`
  INSERT INTO task4_words (word, correct_stress_index, wrong_stress_index, hint, category, is_exception, part_of_speech)
  VALUES (?, ?, ?, ?, ?, ?, ?)
`);

let added = 0;
let skipped = 0;

const insertMany = db.transaction(() => {
  for (const [correct_marked, wrong_marked, hint, category, is_exception, pos] of WORDS) {
    let ci, wi, word;
    try {
      const c = parseStress(correct_marked);
      const w = parseStress(wrong_marked);
      if (c.word !== w.word) {
        console.warn(`⚠  Слова не совпадают: "${correct_marked}" / "${wrong_marked}" — пропуск`);
        skipped++;
        continue;
      }
      word = c.word;
      ci   = c.stressIndex;
      wi   = w.stressIndex;
    } catch (e) {
      console.warn(`⚠  ${e.message} — пропуск`);
      skipped++;
      continue;
    }

    // Пропустить, если уже есть такое слово
    const existing = db.prepare('SELECT id FROM task4_words WHERE word = ?').get(word);
    if (existing) {
      skipped++;
      continue;
    }

    insert.run(word, ci, wi, hint || null, category, is_exception ? 1 : 0, pos);
    added++;
  }
});

insertMany();

