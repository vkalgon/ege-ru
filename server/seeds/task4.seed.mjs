const UPPER_VOWELS = new Set([...'АЕЁИОУЫЭЮЯ']);

function parseStress(marked) {
  let stressIdx = -1;
  let word = '';
  for (let i = 0; i < marked.length; i++) {
    const ch = marked[i];
    if (UPPER_VOWELS.has(ch)) stressIdx = i;
    word += ch.toLowerCase();
  }
  if (stressIdx === -1) throw new Error(`Нет ударения в слове: "${marked}"`);
  return { word, stressIndex: stressIdx };
}

